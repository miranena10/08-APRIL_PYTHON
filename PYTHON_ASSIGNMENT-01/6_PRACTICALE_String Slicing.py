text = "Hello Python"

# Basic slicing
print("Original String:", text)
print("First 5 characters:", text[0:5])

# From beginning
print("From start to index 4:", text[:5])

# Till end
print("From index 6 to end:", text[6:])

# Negative indexing
print("Last 6 characters:", text[-6:])

# Step slicing
print("Every second character:", text[::2])

# Reverse string
print("Reversed string:", text[::-1])